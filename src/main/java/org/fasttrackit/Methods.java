package org.fasttrackit;

public class Methods {

    public int sum(int number1, int number2) {
        return number1 + number2;
    }

    public int difencence(int number1, int number2) {
        return number1 - number2;
    }


    public int multiplication(int number1, int number2) {
        return number1 * number2;
    }

    public int division(int number1, int number2) {
        return number1 / number2;
    }

    public double sum2(double number1, double number2) {
        return number1 + number2;
    }

    public double difencence2(double number1, double number2) {
        return number1 - number2;
    }


    public double multiplication2(double number1, double number2) {
        return number1 * number2;
    }
    public double division2(double number1, double number2) {
        return number1 / number2;
}
}